def triangle(base, height):
    """
    return area of triangle
    """
    area = 0.5 * base * height
    return area


def area_of_square(length):
    """
    return area of square
    """
    area = length * length
    return area


def rectangle(length, width):
    """
     return area of rectangle
     """
    area = length * width
    return area
